def get_zomac_pdet(dpto: str, nom_mpio: str) -> dict:
    """
    Obtiene los valores de ZOMAC y PDET para un municipio dado su departamento y nombre.
    
    Args:
        dpto: Nombre del departamento
        nom_mpio: Nombre del municipio
        
    Returns:
        dict con claves 'zomac' y 'pdet', ambos False si no se encuentra
    """
    dpto_upper = dpto.upper().strip()
    nom_mpio_upper = nom_mpio.upper().strip()
    
    if dpto_upper in MUNICIPIOS_ZOMAC_PDET:
        if nom_mpio_upper in MUNICIPIOS_ZOMAC_PDET[dpto_upper]:
            return MUNICIPIOS_ZOMAC_PDET[dpto_upper][nom_mpio_upper]
    
    return {"zomac": False, "pdet": False}

